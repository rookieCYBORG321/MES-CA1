ORG 0000H

0000  MOV R0, #40H
0002  MOV R1, #40H

SCAN:
0004  MOV A, @R0
0005  INC R0
0006  CJNE A, #0FFH, STORE
0009  SJMP CHECK

STORE:
000B  MOV @R1, A
000C  INC R1

CHECK:
000D  MOV A, R0
000E  CJNE A, #60H, SCAN

FILL:
0011  MOV A, R1
0012  CJNE A, #60H, CLEAR
0015  SJMP DONE

CLEAR:
0017  MOV @R1, #00H
0019  INC R1
001A  SJMP FILL

DONE:
001C  SJMP DONE

END
